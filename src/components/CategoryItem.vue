<template>
  <a href="#" :class="classes">{{ category }}</a>
</template>

<script>
export default {
  name: 'CategoryItem',
  props: ['category', 'isActive'],
  computed: {
    classes() {
      const classes = ['px-3', 'py-1', 'transition', 'bg-gray-600', 'border'];

      return this.isActive
        ? [
            ...classes,
            'border-gray-700',
            'rounded-full',
            'hover:bg-gray-500',
            'text-white',
          ]
        : [...classes, 'border-gray-300', 'bg-gray-100', 'hover:bg-gray-200'];
    },
  },
};
</script>

